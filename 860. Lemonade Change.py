class Solution:
    def lemonadeChange(self, bills: List[int]) -> bool:
        if bills[0] > 5: return False
        five, ten = 0, 0
        for bill in bills:
            if bill == 5:
                five+=1
            elif bill==10:
                ten+=1
                five-=1
            else:
                if ten>0:
                    ten-=1
                    five-=1
                else:
                    five -= 3
            if five < 0:
                return False
        return True


